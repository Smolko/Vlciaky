<h2>Vitajte v databáze jedincov Československého vlčiaka Slovenskej republiky</h2>

<br><br>
<p>Databáza jedincov vznikla v roku 2013, no na jej príprave sa pracovalo dlhé mesiace pred tým. Je určená všetkým majiteľom, chovateľom československého vlčiaka. V databázi sa nachádzajú údaje o psoch narodených na Slovensku a importovaných, zapísaných v SPKP od počiatku kríženia československého vlčiaka po súčastnosť. Za údaje ďakujeme ako chovateľom, tak majiteľom a členom chovateľskej komisie a výboru klubu, ktorí údaje zbierali a archivovali. </p>
<p>Propagácia jedincov a chovateľských staníc je zadarmo a každý má možnosť si aktualizovať informácie o svojich psoch a odchovoch rovnako zadarmo. Prosíme všetkých majiteľov a chovateľov, aby nám zasielali údaje o psoch, aby bola naša databáza čo najpresnejšia a najrozsiahlejšia. A samozrejme, ak by ste našli nejaké chyby, aby ste nám o nich dali vedieť.</p>
<p>Do databázy sa zapisujú len OVERENÉ informácie</p>
<p>Databáza slúži nám chovateľom a chovateľskej komisii, aby pomáhala zlepšiť pohľad plemeno.</p>


<br>
<h3>O databáze - čo všetko dokáže?</h3>
<p>Okrem reprezentatívnej fotografie psa vo výstavnom postoji a tiež jeho hlavy, získate informácie ako sú: meno psa, sfarbenie, pohlavie, dátum narodenia, úmrtia, číslo zápisu, tetovania, čipu, stav chovnosti, informácie o zdravotných vyšetreniach a ich výsledky, výsledky z výstav, bonitácie, SVP, meno majiteľa, kontaktné údaje o majiteľovi, rodokmeň do 4 generácie, potomkovia a mnohé ďalšie.</p>
<p>V databázi je možné vyhľadávať psov podľa mena alebo chovateľskej stanice. Rozšírené hľadanie podľa výsledku dysplázie, chovnosti, roku narodenia,... Pre chovateľov štatistické funkcie a výsledky chovu. Tiež tu sústreďujeme výsledky SVP, bonitácií a výstav.</p>

<br>
<h3>Registrácia</h3>
<p>Registráciu v našom systéme vyžadujeme od každého, kto chce s ňou pracovať. Po potvrdení registrácie a udeleného statusu majiteľ, chovateľ, člen výboru, dostanete svoje oprávnenia na pohyb v databáze. Pre každé zaslanie údajov ako sú zdravotné výsledky, či skúšky, prípadne titul z výstavy, je potrebné zaslanie aj certifikátu, ktorý potvrdí výsledok je rovnako nutné prihlásenie sa. Niektoré údaje budú viditeľné len pre užívateľov na úrovni chovateľ, niektoré pre užívateľov - majiteľov psov a niektoré len pre chovateľskú komisiu.</p>

<br>
<h3>Zasielanie a dopĺňanie informácií</h3>
<p>Informácie ku psom sa zasielajú adminovi databázy, spolu s potvrdzujúcim certifikátom, ktorý ich musí schváliť, aby sa zaručila pravdivosť údajov. Ak máte informácie, ktoré chcete doplniť, alebo zmeniť, prosím kontaktujte nás cez formulár, ktorý je pri každom jedincovi pod tlačítkom "pridať info". V ňom nájdete priestor pre doplnenie informácií a miesto pre nahranie prílohy - certifikátu o pravdivosti údajov - výsledkov zdravotných vyšetrení, zložených skúšok z výkonu, titulov z výstav a pod. Tiež môžete zasielať fotografie k jednotlivcom. Musia byť reprezentatívne. Jedna fotografia je hlava psa, kde vidno uši, celú hlavu, ideálne až po plecia a druhá fotografia je výstavný postoj psa, kde je vidno celé telo jedinca, nie z uhla, nohy (pes nesmie stáť vo vysokej tráve), chvost ideálne v kľude, hlava zboku,prípadne smerujúca k fotografovi. Fotografia nesmie byť rozmazaná, nekvalitná, čiernobiela (výnimku tvoria historické fotografie), upravovaná v programoch na úpravu fotografií (mazanie znamienok, úprava farby filtráciou, rámčeky,...). Farba fotografie musí byť originálna, znázorňujúca skutočnú farbu psa. Informácie doplňujúce, ako plnochruposť, skus, obe varlatá u psov a podobne je nutné doložiť posudkom z výstavy psov, zvodu mladých alebo bonitácie, prípadne potvrdené od veterinára. Inak nebudú akceptované. Strata trvalých, vyrastených zubov úrazom, musí byť doložená potvrdením od veterinára. Certifikáty môžete zasielať aj priamo cez klubový email, no ak je to možné, použite formulár.</p>

<br>
<h4>Dopĺňanie informácií</h4>
<p>Informácie sa snažíme aktualizovať a dopĺňať ihneď po ich obdržaní. Iste si uvedomujete, že ide o kvantum jedincov a ich údajov, ktoré treba pridať. Preto vás prosíme o trpezlivosť. Prednosť budú mať jedinci od roku narodenia 2000 a viac, neskôr starší psy.</p>
<p>Máme však veľký záujem aj o historické údaje, fotografie, ktoré nám rovnako prosím zasielajte cez formulár alebo emailom.</p>

